export function getTranslatedCocktail(cocktail: Cocktail, locale: Locale): Cocktail {
  const translation = cocktailTranslations[cocktail.id];
  
  if (!translation) {
    // ถ้าไม่มีคำแปล ให้คืนค่าต้นฉบับ
    return cocktail;
  }

  const translated = translation[locale];
  
  return {
    ...cocktail,
    name: translated.name,
    description: translated.description,
    ingredients: translated.ingredients,
    preparation: translated.preparation,
    garnish: translated.garnish,
    glassware: translated.glassware,
    method: translated.method,
  };
}
